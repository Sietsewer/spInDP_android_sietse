package com.example.touchscreencontroller;

public class Vector3f_simple {
	public float x;
	public float y;
	public float z;
	
	public Vector3f_simple(){
		this.x = 0.0f;
		this.y = 0.0f;
		this.z = 0.0f;
	}
	
	public Vector3f_simple(float x, float y, float z){
		this.x = x;
		this.y = y;
		this.z = z;
	}
}
